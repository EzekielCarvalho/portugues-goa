  <p  className="historytext">

The Portuguese also helped with bringing about a legacy of rich education by establishing Roman Catholic schools. They helped with the spread of Christianity and as a result many Hindus and Muslims were asked to convert themselves into Catholics. As a result, Goa has a strong Catholic background even though the number of Hindus far exceeds those of Roman Catholics today. Various festivals such as Easter, Carnival and Christmas are celebrated, which have European origin. The Portuguese also had a great influence in the design of local architecture which is still evident today, as well as on food habits and diet.

</p>

